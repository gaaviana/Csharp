
namespace POO
{
    public class Bolo
    {
        public string Sabor;
        public string Recheio;
        public string Cobertura;
        public int Camadas;
        public string Tamanho;

        public Bolo(string sabor, string recheio, string cobertura, int camadas, string tamanho)
        {
            Sabor = sabor;
            Recheio = recheio;
            Cobertura = cobertura;
            Camadas = camadas;
            Tamanho = tamanho;
        }
    }  
}
